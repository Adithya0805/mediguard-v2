# ─────────────────────────────────────────────────────────────────────────────
# Background pipeline task
# ─────────────────────────────────────────────────────────────────────────────

async def run_pipeline_background(
    session_id: str,
    patient_data: dict,
    db: Client,
) -> None:
    """
    Background task: runs the full MediGuard clinical pipeline, persists the
    generated report, and updates session status on both success and failure.

    This function is intentionally fully self-contained with its own
    exception handling — it must never crash silently.
    """
    from app.agents.orchestrator import orchestrator

    logger.info("Background pipeline task starting", session_id=session_id)

    audit_service  = AuditService(db)
    patient_service = PatientService(db, audit_service)
    report_service = ReportService(db, audit_service)

    try:
        # ── Run the full LangGraph multi-agent pipeline ───────────────────────
        final_state = await orchestrator.run_pipeline(session_id, patient_data)

        report_data     = final_state.get("report", {})
        ddx_list        = final_state.get("differential_diagnosis", [])
        drug_interactions = final_state.get("drug_interactions", [])
        urgency_level   = final_state.get("urgency_level", "medium")
        completed_agents = final_state.get("completed_agents", [])

        # Extract recommended tests from the report object or DDx
        recommended_tests = report_data.get(
            "recommended_workup",
            _extract_tests_from_ddx(ddx_list),
        )

        # Build the report persistence payload
        report_payload = {
            "differential_diagnosis":  ddx_list,
            "recommended_tests":       recommended_tests,
            "drug_interactions_found": drug_interactions,
            "clinical_summary":        report_data.get(
                "clinical_narrative",
                report_data.get("executive_summary", "Report generated by MediGuard V2.")
            ),
            "urgency_level":           urgency_level,
            "reviewed_by_agent":       "MediGuardOrchestrator v2",
            "fhir_bundle":             {},
        }

        # ── Persist the clinical report ───────────────────────────────────────
        await report_service.save_report(UUID(session_id), report_payload)

        # ── Generate clinical visual PDF and structured FHIR bundle ──────────────────
        logger.info("Triggering clinical document generation workflow in background task", session_id=session_id)
        from app.services.document_service import DocumentService
        document_service = DocumentService(db)

        # Build comprehensive report payload matching visual components
        doc_report_data = {
            "differential_diagnosis": ddx_list,
            "recommended_workup": recommended_tests,
            "drug_interactions": drug_interactions,
            "urgency_level": urgency_level,
            "urgency_assessment": final_state.get("urgency_assessment", report_data.get("urgency_assessment", f"Urgency level assessed as {urgency_level}.")),
            "executive_summary": report_data.get("executive_summary", "Clinical decision support summary for patient session."),
            "clinical_narrative": report_data.get("clinical_narrative", ""),
            "disposition_recommendation": report_data.get("disposition_recommendation", "N/A"),
            "follow_up_instructions": report_data.get("follow_up_instructions", []),
            "differential_summary": report_data.get("differential_summary", ""),
            "clinical_disclaimers": report_data.get("clinical_disclaimers", [
                "This report is AI-generated clinical decision support only.",
                "All recommendations must be reviewed by a licensed physician.",
                "This system does not replace clinical judgment."
            ]),
            "primary_diagnosis": report_data.get("primary_diagnosis", {}),
            "contraindications": final_state.get("contraindications", report_data.get("contraindications", [])),
            "medication_notes": final_state.get("medication_notes", report_data.get("medication_notes", "")),
            "fda_data_used": final_state.get("fda_data_used", False),
            "report_metadata": {
                "agents_used": completed_agents,
                "generation_time_seconds": final_state.get("report_metadata", {}).get("generation_time_seconds", 15.0),
                "model_used": final_state.get("report_metadata", {}).get("model_used", "MediGuard Clinical AI"),
                "rag_sources_count": final_state.get("report_metadata", {}).get("rag_sources_count", 0)
            }
        }

        doc_res = await document_service.generate_clinical_documents(
            session_id=session_id,
            report_data=doc_report_data,
            patient_data=patient_data
        )

        # ── Mark session as completed ─────────────────────────────────────────
        await patient_service.update_session_status(UUID(session_id), "completed")

        await audit_service.log_action(
            action="pipeline_completed",
            actor="orchestrator",
            session_id=UUID(session_id),
            metadata={
                "agents_completed": completed_agents,
                "urgency_level":    urgency_level,
                "report_generated": final_state.get("report_generated", False),
                "pdf_url":          doc_res.get("pdf_url"),
                "fhir_valid":       doc_res.get("fhir_valid")
            },
        )

        logger.info(
            "Background pipeline task completed successfully with document compilation",
            session_id=session_id,
            urgency=urgency_level,
            agents_run=completed_agents,
            pdf_url=doc_res.get("pdf_url")
        )

    except Exception as exc:
        logger.error(
            "Background pipeline task FAILED",
            session_id=session_id,
            error=str(exc),
        )
        try:
            # Best-effort: update session status to failed so the client knows
            audit_service  = AuditService(db)
            patient_service = PatientService(db, audit_service)
            await patient_service.update_session_status(UUID(session_id), "failed")
            await audit_service.log_action(
                action="pipeline_failed",
                actor="orchestrator",
                session_id=UUID(session_id),
                metadata={"error": str(exc)},
            )
        except Exception as inner_exc:
            logger.error(
                "Could not mark session as failed after pipeline error",
                session_id=session_id,
                inner_error=str(inner_exc),
            )


