@router.post("/generate", status_code=status.HTTP_202_ACCEPTED)
async def generate_report(
    request: ReportRequest,
    background_tasks: BackgroundTasks,
    db: Client = Depends(get_db),
    actor: dict = Depends(verify_clinical_auth),
):
    """
    Trigger the multi-agent CDSS analysis pipeline for a patient session.

    Immediately returns 202 Accepted. The pipeline runs in the background.
    Poll GET /report/{session_id} to check for completion.
    """
    logger.info(
        "Received request to trigger report generation",
        session_id=request.session_id,
    )

    audit_service  = AuditService(db)
    patient_service = PatientService(db, audit_service)
    report_service = ReportService(db, audit_service)

    # 1. Validate patient session exists
    session = await patient_service.get_session(request.session_id, actor=actor.get("username", "system"))

    # 2. Short-circuit if already completed
    if session.status == "completed":
        logger.info(
            "Session already completed — returning existing report reference",
            session_id=request.session_id,
        )
        return JSONResponse(
            status_code=status.HTTP_200_OK,
            content={
                "session_id": str(request.session_id),
                "status":     "completed",
                "message":    "Report already generated. Use GET /report/{session_id} to retrieve.",
            },
        )

    # 3. Short-circuit if already running
    if session.status == "processing":
        logger.info(
            "Pipeline already running for session",
            session_id=request.session_id,
        )
        return JSONResponse(
            status_code=status.HTTP_202_ACCEPTED,
            content={
                "session_id": str(request.session_id),
                "status":     "processing",
                "message":    "Report generation already in progress. Poll GET /report/{session_id}.",
            },
        )

    # 4. Mark session as processing
    await patient_service.update_session_status(request.session_id, "processing")

    # 5. Build patient_data dict from session object for the pipeline
    patient_data = {
        "patient_name":        session.patient_name,
        "patient_age":         session.patient_age,
        "patient_gender":      session.patient_gender,
        "chief_complaint":     session.chief_complaint,
        "symptoms":            session.symptoms,
        "medical_history":     session.medical_history,
        "current_medications": session.current_medications,
        "allergies":           session.allergies,
        "vitals":              session.vitals,
    }

    # 6. Enqueue background pipeline task
    background_tasks.add_task(
        run_pipeline_background,
        str(request.session_id),
        patient_data,
        db,
    )

    logger.info(
        "Pipeline enqueued in background",
        session_id=request.session_id,
    )

    return {
        "session_id": str(request.session_id),
        "status":     "processing",
        "message":    "Report generation initiated. State transitioned to processing.",
    }


@router.get("/{session_id}", status_code=status.HTTP_200_OK)
async def get_report(
    session_id: UUID,
    db: Client = Depends(get_db),
    actor: dict = Depends(verify_clinical_auth),
):
    """
    Poll report completion status.

    Returns the clinical report if completed, or status + polling instructions
    if still processing.
    """
    logger.info(
        "Received request to retrieve clinical report results",
        session_id=session_id,
    )

    audit_service  = AuditService(db)
    patient_service = PatientService(db, audit_service)
    report_service = ReportService(db, audit_service)

    # Check session status first — fail-fast if not ready
    session = await patient_service.get_session(session_id, actor=actor.get("username", "system"))

    if session.status == "processing":
        return JSONResponse(
            status_code=status.HTTP_202_ACCEPTED,
            content={
                "session_id": str(session_id),
                "status":     "processing",
                "message":    "Clinical pipeline is still running. Please try again in a few seconds.",
            },
        )

    if session.status == "failed":
        return JSONResponse(
            status_code=status.HTTP_500_INTERNAL_SERVER_ERROR,
            content={
                "session_id": str(session_id),
                "status":     "failed",
                "message":    "Clinical pipeline failed. Please re-trigger or contact support.",
            },
        )

    report = await report_service.get_report(session_id)
    return report


@router.get("/{session_id}/audit", status_code=status.HTTP_200_OK)
async def get_report_audit(
    session_id: UUID,
    db: Client = Depends(get_db),
    actor: dict = Depends(verify_clinical_auth),
):
    """
    Returns the complete HIPAA action audit trail associated with the session.
    """
    logger.info(
        "Received request to retrieve clinical session audit log trails",
        session_id=session_id,
    )

    audit_service  = AuditService(db)
    patient_service = PatientService(db, audit_service)

    # Validate session exists first
    await patient_service.get_session(session_id, actor=actor.get("username", "system"))

    audit_trail = await audit_service.get_session_audit_trail(session_id)
    return audit_trail


# ── Utility ───────────────────────────────────────────────────────────────────

def _extract_tests_from_ddx(ddx_list: list) -> list:
    """Extract recommended tests from the DDx list as a fallback."""
    tests = []
    for ddx in ddx_list:
        if isinstance(ddx, dict):
            pass  # DDx list doesn't carry tests — just return empty
    return tests


