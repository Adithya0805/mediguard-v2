@router.get("/{session_id}/pdf", status_code=status.HTTP_307_TEMPORARY_REDIRECT)
async def get_report_pdf(
    session_id: UUID,
    db: Client = Depends(get_db),
    actor: dict = Depends(verify_clinical_auth),
):
    """
    Returns a 307 temporary redirect to the compiled PDF report stored in Supabase Storage.
    Returns 404 with error details if the PDF has not been generated yet.
    """
    logger.info("Received request to retrieve PDF report redirect URL", session_id=session_id)
    report_service = ReportService(db, AuditService(db))
    try:
        report = await report_service.get_report(session_id)
        if not report.report_pdf_url:
            return JSONResponse(
                status_code=status.HTTP_404_NOT_FOUND,
                content={"error": True, "message": "PDF not yet generated. Check report status."}
            )
            
        return RedirectResponse(
            url=report.report_pdf_url,
            status_code=status.HTTP_307_TEMPORARY_REDIRECT
        )
    except Exception as e:
        logger.error("Failed to retrieve PDF report redirect URL", session_id=session_id, error=str(e))
        return JSONResponse(
            status_code=status.HTTP_404_NOT_FOUND,
            content={"error": True, "message": "PDF not yet generated. Check report status."}
        )


@router.get("/{session_id}/fhir", status_code=status.HTTP_200_OK)
async def get_fhir_bundle(
    session_id: UUID,
    db: Client = Depends(get_db),
    actor: dict = Depends(verify_clinical_auth),
):
    """
    Returns the raw FHIR bundle JSON object compiled for this session.
    """
    logger.info("Received request to retrieve FHIR bundle JSON", session_id=session_id)
    report_service = ReportService(db, AuditService(db))
    try:
        report = await report_service.get_report(session_id)
        if not report.fhir_bundle:
            return JSONResponse(
                status_code=status.HTTP_404_NOT_FOUND,
                content={"error": True, "message": "FHIR bundle not yet generated. Check report status."}
            )
            
        return JSONResponse(
            content=report.fhir_bundle,
            media_type="application/fhir+json",
            headers={"X-FHIR-Version": "4.0.1"}
        )
    except Exception as e:
        logger.error("Failed to retrieve FHIR bundle JSON", session_id=session_id, error=str(e))
        return JSONResponse(
            status_code=status.HTTP_404_NOT_FOUND,
            content={"error": True, "message": f"FHIR bundle not found. Details: {str(e)}"}
        )

